package com.srinivasa.refrigerationworks.srw.utility.common.enums;

/*
 * Enum for defining response body statuses
 * SUCCESS - Represents a successful operation
 * FAILURE - Represents a failed operation
 * ERROR - Represents an error during the operation
 * NOT_FOUND - Represents a resource not found
 */
public enum ResponseBodyStatus {
    SUCCESS, FAILURE, ERROR, NOT_FOUND;
}
